/**
 * The quick one liner solution with cheat.
 *
 * @param {String} num1
 * @param {String} num2
 * @return {String}
 */
const multiply = (num1, num2) => {
  return (Number(num1) * Number(num2)).toString();
};

/**
 * The quick one liner solution with basic conversion.
 *
 * @param {String} num1
 * @param {String} num2
 * @return {String}
 */
const multiply = (num1, num2) => {
  return ((num1 * 1) * (num2 * 1)).toString();
};
